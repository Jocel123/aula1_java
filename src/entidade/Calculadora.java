package entidade;

public class Calculadora {

	public void somar(int num1, int num2) {
		System.out.println("A soma é: " + (num1 + num2));
	}

	public void subtracao(int num1, int num2) {
		System.out.println("A subtração é: " + (num1 - num2));
	}

	public void divisao(int num1, int num2) {
		System.out.println("A divisao é: " + (num1 / num2));
	}

	public void multiplicacao(int num1, int num2) {
		System.out.println("A multiplicação é: " + (num1 * num2));
	}
}
